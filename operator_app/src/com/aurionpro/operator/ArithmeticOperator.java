package com.aurionpro.operator;

public class ArithmeticOperator {

	public static void main(String[] args) {
		int a = 15;
        int b = 4;
        
        System.out.println("Arithmetic Operators");
        
        // Addition
        int sum = a + b;
        System.out.println("Sum: " + sum);  // Sum: 19

        // Subtraction
        int difference = a - b;
        System.out.println("Difference: " + difference);  // Difference: 11

        // Multiplication
        int product = a * b;
        System.out.println("Product: " + product);  // Product: 60

        // Division (Quotient)
        int quotient = a / b;
        System.out.println("Quotient: " + quotient);  // Quotient: 3

        // Modulus (remainder)
        int remainder = a % b;
        System.out.println("Remainder: " + remainder);  // Remainder: 3


	}

}
