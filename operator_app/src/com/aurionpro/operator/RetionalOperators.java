package com.aurionpro.operator;

public class RetionalOperators {

	public static void main(String[] args) {
		 int a = 15;
	     int b = 20;

	        // Equal to
	        boolean isEqual = (a == b);
	        System.out.println("Is equal: " + isEqual);  // Is equal: false

	        // Not equal to
	        boolean isNotEqual = (a != b);
	        System.out.println("Is not equal: " + isNotEqual);  // Is not equal: true

	        // Greater than
	        boolean isGreater = (a > b);
	        System.out.println("Is greater: " + isGreater);  // Is greater: false

	        // Less than
	        boolean isLess = (a < b);
	        System.out.println("Is less: " + isLess);  // Is less: true

	        // Greater than or equal to
	        boolean isGreaterOrEqual = (a >= 15);
	        System.out.println("Is greater or equal: " + isGreaterOrEqual);  // Is greater or equal: true

	        // Less than or equal to
	        boolean isLessOrEqual = (a <= b);
	        System.out.println("Is less or equal: " + isLessOrEqual);  // Is less or equal: true
	    
	}

}
