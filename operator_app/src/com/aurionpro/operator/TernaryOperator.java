package com.aurionpro.operator;

public class TernaryOperator {

	public static void main(String[] args) {
		
		        int a = 10;
		        int b = 20;

		        // Using the ternary operator
		        int max = (a > b) ? a : b;  
		        System.out.println("The maximum value is: " + max);  
	}

}
