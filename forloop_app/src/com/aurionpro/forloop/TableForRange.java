package com.aurionpro.forloop;


public class TableForRange {

	public static void main(String[] args) {
//		
		 
		 for(int j = 1 ; j <= 10; j++) {
			 for(int i = 1 ; i <= 10; i++) {
				 int mulplication = j*i;
				 System.out.println(j+" X "+i+" = "+mulplication);
				
			 }
			 System.out.println("-----------");
		}
	}

}
